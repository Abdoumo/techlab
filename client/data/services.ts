export function getServicesByCategory(categoryId: string): Service[] {
  return servicesData.filter((service) => service.categoryId === categoryId);
}

export function getServiceById(serviceId: string): Service | undefined {
  return servicesData.find((service) => service.id === serviceId);
}

export function getAllCategories(): Array<{ id: string; name: string }> {
  const categories = new Map<string, string>();
  servicesData.forEach((service) => {
    if (!categories.has(service.categoryId)) {
      categories.set(service.categoryId, service.category);
    }
  });
  return Array.from(categories.entries()).map(([id, name]) => ({ id, name }));
}
