// Helper function to generate SVG gradient placeholder
const generatePlaceholderSVG = (color: string, text: string): string => {
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="500" height="300">
    <defs>
      <linearGradient id="grad" x1="0%" y1="0%" x2="100%" y2="100%">
        <stop offset="0%" style="stop-color:${color};stop-opacity:1" />
        <stop offset="100%" style="stop-color:${color}dd;stop-opacity:1" />
      </linearGradient>
    </defs>
    <rect width="500" height="300" fill="url(#grad)"/>
    <text x="50%" y="50%" font-size="48" font-weight="bold" fill="#ffffff" text-anchor="middle" dominant-baseline="middle" font-family="Arial, sans-serif">${text}</text>
  </svg>`;

  return `data:image/svg+xml;base64,${btoa(svg)}`;
};

